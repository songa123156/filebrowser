import json
import sys

def drill(parent, data):
    if isinstance(data, dict):
        if parent:
            parent = parent + '.'
        for key, val in data.items():
            new_parent = parent + key
            drill(new_parent, val)
    elif isinstance(data, list):
        print(parent + '[#]', '=', str(len(data)))
        if parent:
            parent = parent + '['
        for index, val in enumerate(data):
            new_parent = parent + str(index) + ']'
            drill(new_parent, val)
    elif not 'passw' in (parent + str(data)).lower():
        print(parent, '=', str(data))


data=json.loads(sys.stdin.read())
drill('', data)


